#include "MyMath.hpp"

/*************************************************************************************************************************************/
                                                                                                                            //求斜率
float myk(float x1,float y1,float x2,float y2){
    float k = 0;
    if(x1 != x2)
        k = (y1 - y2)/(x1 - x2);
    else{
        if(y1 > y2)
            k = -2000;
        else if(y1 < y2)
            k = 2000;
        else
            k = 0;
    }

    if(k > 1000){
        k = 1000;
    }else if(k < -1000){
        k = -1000;
    }
    return k;
}
/*************************************************************************************************************************************/
                                                                                                                            //求b
float myb(float x1,float y1,float x2,float y2){
    float b = y1 - myk( x1, y1, x2, y2)*x1;
    return b;
}
